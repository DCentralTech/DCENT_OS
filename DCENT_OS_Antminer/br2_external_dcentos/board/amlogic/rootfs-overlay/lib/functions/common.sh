#!/bin/sh
#
# DCENT_OS Amlogic NAND layout constants.
#
# These values describe the BOS/DCENT single-system Amlogic layout captured on
# am3-aml. They are shared by first-boot recovery and optional post-install
# hooks. Scripts that source this file must still verify the resolved MTD names
# before writing.

DCENTOS_ENV_FILE=nand_env
DCENTOS_UBOOT_FILE=u-boot.bin
DCENTOS_DTB_FILE=dtb
DCENTOS_KERNEL_FILE=uImage
DCENTOS_ROOTFS_FILE=rootfs.img

DCENTOS_INSTALL_LOG=/config/install.log
DCENTOS_ENV_STOCK_CONFIG=stock_config
DCENTOS_NAND_ENV_DEVICE=/dev/nand_env

DCENTOS_NAND_ENV_STOCK_FILE=/tmp/nand_env_stock
DCENTOS_ENV_TAR=/tmp/env.tar
DCENTOS_POST_INSTALL_TAR=/tmp/ro-post_install.tar

DCENTOS_POST_INSTALL_DIR=post_install
DCENTOS_POST_INSTALL_SCRIPT=run.sh

DCENTOS_GLOBAL_UBOOT_OFFSET=0x000007000000
DCENTOS_GLOBAL_DTB_OFFSET=0x000007500000
DCENTOS_GLOBAL_KERNEL_OFFSET=0x000007800000
DCENTOS_GLOBAL_OVERLAY_OFFSET=0x000004700000
DCENTOS_GLOBAL_ROOTFS_OFFSET=0x00000B800000

DCENTOS_GLOBAL_RO_ENV_TAR_LENGTH_OFFSET=0x000009800000
DCENTOS_LOCAL_RO_ENV_TAR_OFFSET=0x000000010000
DCENTOS_GLOBAL_RO_ENV_TAR_OFFSET=$(printf "0x%X" $(($DCENTOS_GLOBAL_RO_ENV_TAR_LENGTH_OFFSET + $DCENTOS_LOCAL_RO_ENV_TAR_OFFSET)))

DCENTOS_GLOBAL_RO_POST_INSTALL_TAR_LENGTH_OFFSET=0x000009C00000
DCENTOS_LOCAL_POST_INSTALL_TAR_LENGTH_OFFSET=0x000000010000
DCENTOS_GLOBAL_RO_POST_INSTALL_TAR_OFFSET=$(printf "0x%X" $(($DCENTOS_GLOBAL_RO_POST_INSTALL_TAR_LENGTH_OFFSET + $DCENTOS_LOCAL_POST_INSTALL_TAR_LENGTH_OFFSET)))
DCENTOS_POST_INSTALL_TAR_MAX_DECIMAL_SIZE=20905984
DCENTOS_POST_INSTALL_TAR_BLOCK_COUNT=160

DCENTOS_GLOBAL_RO_NAND_ENV_OFFSET=0x00000B000000
DCENTOS_GLOBAL_RW_FLAGS_OFFSET=0x00000B400000

DCENTOS_STOCK_ENV_LENGTH=65536

DCENTOS_SYSTEM_MTD_NAME=system
DCENTOS_OVERLAY_MTD_NAME=overlay
DCENTOS_OVERLAY_UBI=/dev/ubi0_0

DCENTOS_RECOVERY_FLAG_INSTALLED=0x1
DCENTOS_RECOVERY_FLAG_FIRST_BOOT=0x2
DCENTOS_RECOVERY_FLAG_SUCCESSFUL=0x3

# Compatibility aliases for scripts ported from BOS naming.
BOS_ENV_FILE=$DCENTOS_ENV_FILE
BOS_UBOOT_FILE=$DCENTOS_UBOOT_FILE
BOS_DTB_FILE=$DCENTOS_DTB_FILE
BOS_KERNEL_FILE=$DCENTOS_KERNEL_FILE
BOS_ROOTFS_FILE=$DCENTOS_ROOTFS_FILE
NAND_ENV_DEVICE=$DCENTOS_NAND_ENV_DEVICE
BOS_ENV_STOCK_CONFIG=$DCENTOS_ENV_STOCK_CONFIG
BOS_SYSTEM_MTD_NAME=$DCENTOS_SYSTEM_MTD_NAME
BOS_OVERLAY_MTD_NAME=$DCENTOS_OVERLAY_MTD_NAME
BOS_OVERLAY_UBI=$DCENTOS_OVERLAY_UBI
RECOVERY_FLAG_INSTALLED=$DCENTOS_RECOVERY_FLAG_INSTALLED
RECOVERY_FLAG_FIRST_BOOT=$DCENTOS_RECOVERY_FLAG_FIRST_BOOT
RECOVERY_FLAG_SUCCESSFUL=$DCENTOS_RECOVERY_FLAG_SUCCESSFUL
POST_INSTALL_DIR=$DCENTOS_POST_INSTALL_DIR
POST_INSTALL_SCRIPT=$DCENTOS_POST_INSTALL_SCRIPT
POST_INSTALL_TAR_BLOCK_COUNT=$DCENTOS_POST_INSTALL_TAR_BLOCK_COUNT
POST_INSTALL_TAR_MAX_DECIMAL_SIZE=$DCENTOS_POST_INSTALL_TAR_MAX_DECIMAL_SIZE
